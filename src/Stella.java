public class Stella {
    private String typeOfDog;
    private int ageOfDogInWeeks;
    private String colorsOfDog;

    public Stella(){
    }

    public String getTypeOfDog() {
        return typeOfDog;
    }

    public void setTypeOfDog(String typeOfDog) {
        this.typeOfDog = typeOfDog;
    }

    public int getAgeOfDogInWeeks() {
        return ageOfDogInWeeks;
    }

    public void setAgeOfDogInWeeks(int ageOfDogInWeeks) {
        this.ageOfDogInWeeks = ageOfDogInWeeks;
    }

    public String getColorsOfDog() {
        return colorsOfDog;
    }

    public void setColorsOfDog(String colorsOfDog) {
        this.colorsOfDog = colorsOfDog;
    }



}
